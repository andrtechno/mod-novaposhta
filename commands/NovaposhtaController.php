class NovaposhtaController extends ConsoleController
{
    /**
     * @var Novaposhta
     */
    private $api;

    public function beforeAction($action)
    {
        $this->api = Yii::$app->novaposhta;
        return parent::beforeAction($action); // TODO: Change the autogenerated stub
    }

    public function actionCities()
    {
        Yii::$app->db->createCommand()->truncateTable(Cities::tableName())->execute();
        $cities = $this->api->getCities();


        if ($cities['success']) {
            $data = [];
            foreach ($cities['data'] as $k => $city) {
                $data[] = [
                    $city['Description'],
                    $city['DescriptionRu'],
                    $city['Ref'],
                    $city['Delivery1'],
                    $city['Delivery2'],
                    $city['Delivery3'],
                    $city['Delivery4'],
                    $city['Delivery5'],
                    $city['Delivery6'],
                    $city['Delivery7'],
                    $city['Area'],
                    $city['SettlementType'],
                    $city['IsBranch'],
                    $city['PreventEntryNewStreetsUser'],
                    $city['Conglomerates'],
                    $city['CityID'],
                    $city['SettlementTypeDescription'],
                    $city['SettlementTypeDescriptionRu'],
                    $city['SpecialCashCheck'],
                    $city['Postomat'],
                    $city['AreaDescription'],
                    $city['AreaDescriptionRu'],
                ];
            }


            Yii::$app->db->createCommand()->batchInsert(Cities::tableName(), [
                'Description',
                'DescriptionRu',
                'Ref',
                'Delivery1',
                'Delivery2',
                'Delivery3',
                'Delivery4',
                'Delivery5',
                'Delivery6',
                'Delivery7',
                'Area',
                'SettlementType',
                'IsBranch',
                'PreventEntryNewStreetsUser',
                'Conglomerates',
                'CityID',
                'SettlementTypeDescriptionRu',
                'SettlementTypeDescription',
                'SpecialCashCheck',
                'Postomat',
                'AreaDescription',
                'AreaDescriptionRu',
            ], $data)->execute();
        }
    }

    public function actionWarehouses()
    {


        Yii::$app->db->createCommand()->truncateTable(Warehouses::tableName())->execute();

        $result = $this->api
            ->model('Address')
            ->method('getWarehouses')
            ->execute();

        $list = [];
        foreach ($result['data'] as $d) {
            $list[] = array_values($d);
        }
        Yii::$app->db->createCommand()->batchInsert(Warehouses::tableName(), [
            'SiteKey',
            'Description',
            'DescriptionRu',
            'ShortAddress',
            'ShortAddressRu',
            'Phone',
            'TypeOfWarehouse',
            'Ref',
            'Number',
            'CityRef',
            'CityDescription',
            'CityDescriptionRu',
            'SettlementRef',
            'SettlementDescription',
            'SettlementAreaDescription',
            'SettlementRegionsDescription',
            'SettlementTypeDescription',
            'Longitude',
            'Latitude',
            'PostFinance',
            'BicycleParking',
            'PaymentAccess',
            'POSTerminal',
            'InternationalShipping',
            'SelfServiceWorkplacesCount',
            'TotalMaxWeightAllowed',
            'PlaceMaxWeightAllowed',
            'Reception',
            'Delivery',
            'Schedule',
            'DistrictCode',
            'WarehouseStatus',
            'WarehouseStatusDate',
            'CategoryOfWarehouse',
            'Direct',
        ], $list)->execute();
    }


    public function actionArea()
    {


        Yii::$app->db->createCommand()->truncateTable(Area::tableName())->execute();

        $result = $this->api
            ->model('Address')
            ->method('getAreas')
            ->execute();


        if ($result['success']) {
            $list = [];
            foreach ($result['data'] as $d) {
                $list[] = array_values($d);
            }
            Yii::$app->db->createCommand()->batchInsert(Area::tableName(), [
                'Ref',
                'AreasCenter',
                'DescriptionRu',
                'Description',
            ], $list)->execute();
        }
    }


